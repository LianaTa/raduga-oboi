     	<p>Фотообои <b>"Антимаркер"</b> - гладкие обои со специальным защитным покрытием, которые не впитывают запахи, устойчивы к механическим повреждениям, можно мыть мыльным раствором, часто используются в детских комнатах.</p>
     	<p id = "mazki_kisti">Текстура <b>"мазки кисти"</b> - это уникальная и очень креативная текстура виниловых обоев на бумажной основе. С ее помощью можно создать неповторимую атмосферу в комнате. На поверхности обоев видны отчетливые мазки кистью, 
        что будет удачно смотреться на дизайнерских фотообоях, которые широко представлены в нашем каталоге. Эта текстура подойдет для любой комнаты, она дополнительно оживит сюжет и позволит ему «дышать».</p>
     	<p>Текстура <b>"холст обойный"</b> - виниловые обои на бумажной основе. Возможность использовать текстуру под холст, позволяет придать фотообоям вид дорогого полотна. Если совместить эту текстуру с сюжетом картины или пейзажа – 
     	 получаем шедевр прямо у себя дома. Такая комбинация изображения и текстуры отлично подчеркнет современный интерьер комнаты.</p>
     	<p id = "len">Текстура <b>"лен"</b> - виниловые обои на бумажной основе. Как известно, лен используется для живописи, а здесь льняная текстура удачно подчеркивает художественность сюжетов с природой. Изображение приобретает глубину и смысл, 
        создается впечатление, что картина вот-вот оживет. Для этой текстуры подойдет раздел Арт, Природа и прочие, на Ваше усмотрение.</p>
     	<p id = "pesok" >Текстура <b>"песок"</b> - виниловые обои на бумажной основе. Эта текстура фотообоев является очень востребованной. Она придает мягкость сюжету, а также некоторую объемность и реалистичность. Данная текстура подойдет для любого 
        интерьера, в котором требуется наличие изюминки или яркого акцента. </p>
